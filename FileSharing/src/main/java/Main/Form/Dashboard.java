            JOptionPane.showMessageDialog(this, "T·∫£i file th√†nh c√¥ng v√†o th∆∞ m·ª•c: " + uploadDir.getPath());

            // C·∫≠p nh·∫≠t danh s√°ch file tr√™n b·∫£ng
